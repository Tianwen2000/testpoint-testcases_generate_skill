def make_id() -> str:
    return "t-" + uuid.uuid4().hex


def topic_xml(item: dict) -> str:
    title = html.escape(item["title"], quote=True)
    children = item.get("children") or []
    body = f'<topic id="{make_id()}" timestamp="{int(time.time() * 1000)}"><title>{title}</title>'
    if children:
        body += '<children><topics type="attached">'
        body += "".join(topic_xml(child) for child in children)
        body += "</topics></children>"
    body += "</topic>"
    return body


def content_xml(tree: dict, sheet_title: str) -> str:
    return (
        '<?xml version="1.0" encoding="UTF-8" standalone="no"?>\n'
        '<xmap-content xmlns="urn:xmind:xmap:xmlns:content:2.0" version="2.0">'
        f'<sheet id="{make_id()}" timestamp="{int(time.time() * 1000)}">'
        f"{topic_xml(tree)}"
        f"<title>{html.escape(sheet_title, quote=True)}</title>"
        "</sheet>"
        "</xmap-content>"
    )


STYLES_XML = (
    '<?xml version="1.0" encoding="UTF-8" standalone="no"?>\n'
    '<xmap-styles xmlns="urn:xmind:xmap:xmlns:style:2.0" version="2.0">'
    "<styles></styles>"
    "</xmap-styles>"
)

META_XML = (
    '<?xml version="1.0" encoding="UTF-8" standalone="no"?>\n'
    '<meta xmlns="urn:xmind:xmap:xmlns:meta:2.0" version="2.0">'
    "<Creator><Name>Codex</Name><Version>GPT-5</Version></Creator>"
    f"<Created>{int(time.time() * 1000)}</Created>"
    "</meta>"
)

MANIFEST_XML = (
    '<?xml version="1.0" encoding="UTF-8" standalone="no"?>\n'
    '<manifest xmlns="urn:xmind:xmap:xmlns:manifest:1.0">'
    '<file-entry full-path="/" media-type="application/vnd.xmind.workbook"/>'
    '<file-entry full-path="content.xml" media-type="text/xml"/>'
    '<file-entry full-path="styles.xml" media-type="text/xml"/>'
    '<file-entry full-path="meta.xml" media-type="text/xml"/>'
    '<file-entry full-path="META-INF/manifest.xml" media-type="text/xml"/>'
    "</manifest>"
)


def count_nodes(item: dict) -> int:
    return 1 + sum(count_nodes(child) for child in item.get("children", []))


def write_xmind(module: str, tree: dict) -> Path:
    OUT_DIR.mkdir(exist_ok=True)
    filename = f"音创AI_MelodAI_V1.8.0_{module}_测试点.xmind"
    path = OUT_DIR / filename
    if path.exists():
        path.unlink()
    with zipfile.ZipFile(path, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("content.xml", content_xml(tree, module))
        zf.writestr("styles.xml", STYLES_XML)
        zf.writestr("meta.xml", META_XML)
        zf.writestr("META-INF/manifest.xml", MANIFEST_XML)
    return path


def main() -> None:
    for module, tree in TREES.items():
        path = write_xmind(module, tree)
        print(f"{module}\t{count_nodes(tree)}\t{path}")


if __name__ == "__main__":
    os.chdir(Path(__file__).resolve().parents[1])
    main()
